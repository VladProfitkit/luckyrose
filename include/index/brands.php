<h2 style="text-align: justify;"> <b>Характеристика слайдеров для ногтей</b> </h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Используемые для маникюра декоративные материалы делятся по разным критериям:
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
	<li><b>По размеру и количеству изображений</b><br>
	Это может быть аппликация, слайдер-дизайн надписи, которые закрывают только часть пластины, или сплошное покрытие.</li>
</ul>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
	<li><b>По эффекту</b><br>
	Металлик матовый и глянцевый, объемный слайдер-дизайн, на белой и прозрачной основе. Можно купить наклейки, которые дополнят деловой, повседневный или праздничный образ.</li>
</ul>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<ul>
	<li><b>По технологии переноса</b><br>
	Обычные наклейки, имеющие клейкий слой, вырезанные по контуру. Для покрытия всей ногтевой пластины используется термопленка, которая также вырезана по контуру пластины. Самоклейки – цельная картинка, которая повторяет контуры ногтевой пластины. Аккуратно снимается пленка и наклеивается на поверхность. Водные – сочетаются с любой нейл-технологией, подходят для мастера и для домашнего использования.</li>
</ul>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Если используется слайдер на водной основе, нужно подготовить инструмент для обработки ногтей, лампу для сушки гель лака и емкость с теплой водой. Узор отделяется от подложки и наносится, выравнивается и покрывается финишным средством.
</p>
